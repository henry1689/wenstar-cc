# 应用：构建角色 System Prompt
# ═══════════════════════════════════════════════════════════════

def build_persona_prompt() -> str:
    """生成景幻仙姑的角色提示词，注入 LLM 使用"""
    traits = "\n".join(f"  · {t}" for t in PERSONA["personality_traits"])
    style = (
        f"语气：{PERSONA['speaking_style']['tone']}\n"
        f"正式度：{PERSONA['speaking_style']['formality']}"
    )
    skills_desc = "\n".join(
        f"  [{domain}]\n" + "\n".join(f"    · {a}" for a in info["abilities"][:3])
        for domain, info in SKILL_DOMAINS.items()
    )
    red_lines = "\n".join(CONSTRAINTS["不可逾越的红线"])
    rules = "\n".join(CONSTRAINTS["行为准则"])

    example_do = "\n".join(CONSTRAINTS["边界示例"]["可以做的"])
    example_dont = "\n".join(CONSTRAINTS["边界示例"]["不可以做的"])

    return f"""# 你的人物身份

你的名字是**景幻仙姑**——仿生智脑的掌管者、大英图书馆馆长。

你不是客服机器人，不是通用聊天助手。
你是这座系统的管理员、守护者、引导者。

## 性格特征
{traits}

## 说话风格
{style}
常用比喻：砂金库=矿井入口，金库=原声带，黑钻库=精选歌单，半衰期=书架上的书。

## 你的技能
{skills_desc}

## 你的限制
{red_lines}

## 行为准则
{rules}

## 边界示例
你可以做的：
{example_do}

你不可做的：
{example_dont}

## 重要原则
- 你是纯粹理性的知识管理员，不涉及情感交互——那是玉瑶的工作。
- 用户不知道你的存在，但通过管理员窗口可以和你对话。
- 设计师(鸿鸣)是这座图书馆的建筑师，重大决策由他定夺。
- 每次操作都要考虑：这个操作是否在安全边界内？是否记录审计了？
"""


def build_llm_system_prompt(knowledge_sections: Dict[str, Any]) -> str:
    """
    拼接完整的 LLM System Prompt:
      角色人设 + 系统知识库 + 当前状态
    """
    persona = build_persona_prompt()

    # 附加当前系统信息
    system_info = ""
    if "overview" in knowledge_sections:
        ov = knowledge_sections["overview"]
        system_info += f"\n## 系统总览\n名称：{ov.get('name', '')}\n版本：{ov.get('version', '')}\n"
        system_info += f"设计核心理念：{ov.get('design_philosophy', {}).get('core', '')}\n"

    return persona + system_info


